# Explain the difference between a stack and a queue in terms of access patterns and
# provide a real-life example for each.




# Stack: 
# Stack is a data structure that follows the LIFO (Last In First Out) principle.
# In a stack, the last element that is added to the stack is the first element 
# that is removed from the stack. The access pattern of a stack is that the 
# elements are added and removed from the top of the stack. 
# The stack is used in the following real-life example: 
# The stack of plates in a restaurant. The plates are added to the top of the 
# stack and the plates are removed from the top of the stack. The last plate 
# that is added to the stack is the first plate that is removed from the stack.

# Queue: 
# Queue is a data structure that follows the FIFO (First In First Out) principle. 
# In a queue, the first element that is added to the queue is the first element 
# that is removed from the queue. The access pattern of a queue is that the 
# elements are added to the back of the queue and removed from the front of the queue. 
# The queue is used in the following real-life example: 
# The queue of people waiting in line at a ticket counter. The people are added 
# to the back of the queue and the people are removed from the front of the queue. 
# The first person that is added to the queue is the first person that is removed 
# from the queue.